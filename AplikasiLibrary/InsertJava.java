    public void dataTable(){
        Object[] Baris = {"id_buku","Judul","Pengarang","Kategori","Quantity"};
        DefaultTableModel tabmod = new DefaultTableModel(null,Baris);
        jTable1.setModel(tabmod);
        
        try{
            rs = stmt.executeQuery("SELECT * FROM buku");
            while(rs.next()){
            String a = rs.getString("id_buku");
            String b = rs.getString("judul");
            String c = rs.getString("pengarang");
            String d = rs.getString("kategori");
            String e = Integer.toString(rs.getInt("qty"));
            String[] data = {a,b,c,d,e};
            tabmod.addRow(data);
          }
        }catch(Exception e){}
    }
    
    

